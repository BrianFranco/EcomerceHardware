CREATE PROCEDURE spCargarCompra
(
@FECHA_V VARCHAR(10),
@DNI_USU_V INT,
@DIRECCION_V VARCHAR(30),
@HORARIO_V VARCHAR(30),
@TOTAL_V float
)
AS
INSERT INTO Ventas
(
Fecha_V,
Dni_Usu_V,
Direccion_V,
Horario_V,
Total_V
)
VALUES
(
@FECHA_V,
@DNI_USU_V,
@DIRECCION_V,
@HORARIO_V,
@TOTAL_V
)RETURN

GO

